<template>
  <v-dialog v-model="voucherStore.cfDialog" width="600px">
    <v-card>
      <v-card-title>
        <span class="text-h5">Do you want to purchase this voucher?</span>
      </v-card-title>
      <v-card-text />
      <v-card-actions>
        <v-spacer />
        <v-btn
          color="green-darken-1"
          variant="text"
          @click="voucherStore.cfDialog = false"
        >
          No
        </v-btn>
        <v-btn color="green-darken-1" variant="text" @click="buy"> Yes </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script>
import { mapStores } from "pinia";
import { voucherStore } from "../../stores/voucherStore";
export default {
  computed: {
    ...mapStores(voucherStore),
  },
  methods: {
    buy() {
      this.voucherStore.cfDialog = false;
      this.voucherStore.purchaseVoucher();
      this.$router.push("/Home");
    },
  },
};
</script>

